-- ============================================================================
-- OPTIONS CONFIGURATION
-- ============================================================================

vim.cmd("let g:netrw_liststyle = 3")

local opt = vim.opt

-- ============================================================================
-- НУМЕРАЦИЯ СТРОК (Line Numbers)
-- ============================================================================

opt.relativenumber = true -- относительная нумерация для быстрой навигации
opt.number = true -- абсолютная нумерация в текущей строке

-- ============================================================================
-- ТАБУЛЯЦИЯ И ОТСТУПЫ (Tabs & Indentation)
-- ============================================================================

opt.tabstop = 2 -- размер табулятора: 2 пробела
opt.shiftwidth = 2 -- ширина отступа: 2 пробела
opt.expandtab = true -- преобразовывать табы в пробелы
opt.autoindent = true -- автоматический отступ при переходе на новую строку
opt.smartindent = true -- умный отступ для правильного форматирования кода

-- ============================================================================
-- ОТОБРАЖЕНИЕ (Display)
-- ============================================================================

opt.wrap = false -- отключить перенос длинных строк
opt.cursorline = true -- подсвечивать текущую строку
opt.signcolumn = "yes" -- всегда показывать колонку для значков (git, диагностика)
opt.cmdheight = 1 -- высота командной строки
opt.completeopt = { "menu", "menuone", "noselect" } -- настройки автодополнения
opt.foldcolumn = "1" -- всегда отображать одну колонку слева для индикации свёрнутых блоков (folds).
opt.foldlevel = 99 -- открыть все свёрнутые блоки при открытии файла
opt.foldlevelstart = 99 -- открыть все свёрнутые блоки при запуске Neovim
opt.foldenable = true -- включить сворачивание кода по умолчанию

-- ============================================================================
-- ПОИСК (Search)
-- ============================================================================

opt.ignorecase = true -- поиск без учёта регистра
opt.smartcase = true -- учитывать регистр, если в запросе есть заглавные буквы
opt.incsearch = true -- показывать результаты поиска по мере ввода
opt.hlsearch = true -- подсвечивать все совпадения в поиске

-- ============================================================================
-- ЦВЕТА И ТЕМА (Colors & Theme)
-- ============================================================================

opt.termguicolors = true -- 24-битный цвет (требуется поддержка в терминале)
opt.background = "dark" -- тёмная цветовая схема

-- ============================================================================
-- БУФЕР ОБМЕНА (Clipboard)
-- ============================================================================

opt.clipboard:append("unnamedplus") -- использовать системный буфер обмена

-- ============================================================================
-- ОКНА И СПЛИТЫ (Windows & Splits)
-- ============================================================================

opt.splitright = true -- вертикальные сплиты открываются справа
opt.splitbelow = true -- горизонтальные сплиты открываются снизу
opt.equalalways = true -- автоматически выравнивать размер окон

-- ============================================================================
-- ОБЩИЕ НАСТРОЙКИ (General)
-- ============================================================================

opt.backspace = "indent,eol,start" -- backspace работает на отступах, конце и начале строки
opt.mouse = "a" -- поддержка мыши
opt.swapfile = false -- отключить swap-файлы
opt.undofile = true -- сохранять историю отмен между сеансами
opt.timeoutlen = 300 -- время ожидания для mapped sequences (мс)
opt.updatetime = 250 -- время обновления для диагностики и gitsigns (мс)

-- ============================================================================
-- ПРОИЗВОДИТЕЛЬНОСТЬ (Performance)
-- ============================================================================

opt.lazyredraw = true -- не перерисовывать экран при выполнении макросов
opt.synmaxcol = 500 -- не подсвечивать синтаксис для очень длинных строк
